void draw(WINDOW *win, int map[30][60], Snake& snake) {
    int ch;
    for (int i = 0; i<30; i++) {
        for(int j = 0; j < 60; j++){
            switch(map[i][j]) {
                case 0:
                    ch = ' ';
                    break;
                case 1:
                    ch = '*';
                    break;
                case 2:
                    ch = '+';
                    break;
                case 3:
                    ch = '@';
                    break;
            }
            mvwaddch(win, i, j, ch);
        }
    }
    mvwprintw(win, snake.getWidth(), snake.getHeight(), "###");

}

